# ==========================================================
# MNC INTERVIEW QUESTIONS & ANSWERS
# ==========================================================
# Q1. Why does LangChain use message objects (like HumanMessage) instead of dictionaries?
# A:  Using typed classes ensures consistent schemas across different LLM backends (OpenAI, Anthropic, etc.).
#     It makes it easier to serialize/deserialize history and validates the structure before sending.
#
# Q2. What is the role of a `ToolMessage`?
# A:  When an LLM requests a tool call (via an AIMessage with `tool_calls` field), the system runs the tool
#     and returns the result inside a `ToolMessage`. The `ToolMessage` must include the matching `tool_call_id`
#     so the LLM knows which specific request this output belongs to.
